package dev.sanmer.geomag;

public class MagneticField {
    private double x;
    private double xDot;
    private double y;
    private double yDot;
    private double z;
    private double zDot;
    private double h;
    private double hDot;
    private double f;
    private double fDot;
    private double d;
    private double dDot;
    private double i;
    private double iDot;

    public double getX() {
        return x;
    }

    public double getXDot() {
        return xDot;
    }

    public double getY() {
        return y;
    }

    public double getYDot() {
        return yDot;
    }

    public double getZ() {
        return z;
    }

    public double getZDot() {
        return zDot;
    }

    public double getH() {
        return h;
    }

    public double getHDot() {
        return hDot;
    }

    public double getF() {
        return f;
    }

    public double getFDot() {
        return fDot;
    }

    public double getD() {
        return d;
    }

    public double getDDot() {
        return dDot;
    }

    public double getI() {
        return i;
    }

    public double getIDot() {
        return iDot;
    }
}
